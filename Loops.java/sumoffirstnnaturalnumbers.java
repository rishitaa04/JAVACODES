public class sumoffirstnnaturalnumbers {
    public static void main(String args[]) {
        int sum = 0;
        int a = 5;
        int i = 1;
        while (i <= a) {
            sum = sum + i;
            // System.out.println(sum);
            i++;
        }
        System.out.println(sum);
    }
}
