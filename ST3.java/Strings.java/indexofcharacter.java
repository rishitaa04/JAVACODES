import java.util.*;

public class indexofcharacter {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        String str = sc.nextLine();
        for (int i = 0; i < str.length(); i++) {
            System.out.println(i);
        }
        sc.close();
    }
}
